/* Logical Operator: 
logical and (&&),
logical or (||),
logical not (!)
*/

let a = 9;
let b = 3;

let cond1 = a > b;
let cond2 = a < b;

console.log("cond1 && cond2 :", cond1 && cond2);                       //output: false
console.log("cond1 || cond2 :", cond1 || cond2);                       //output: true