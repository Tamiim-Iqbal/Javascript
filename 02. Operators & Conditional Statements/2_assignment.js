/* Assignment Operator:
=, +=, -=, *=, /=, %=, **= 
*/

let a = 8;
let b = 3;

console.log("a = a + b :", a += b);                          //output: 11                   
console.log("a = a - b :", a -= b);                          //output: 8
console.log("a = a * b :", a *= b);                          //output: 24
console.log("a = a / b :", a /= b);                          //output: 8
console.log("a = a % b :", a %= b);                          //output: 2
console.log("a = a ** b :", a **= b);                        //output: 8