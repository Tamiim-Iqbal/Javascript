/* Arithmetic Operator: 
+, -, *, /, modulus(%), exponential(**)
*/

let a = 10;
let b = 4;

console.log("a + b :", a + b);                              //output: 14                   
console.log("a - b :", a - b);                              //output: 6
console.log("a * b :", a * b);                              //output: 40
console.log("a / b :", a / b);                              //output: 2.5
console.log("a % b :", a % b);                              //output: 2
console.log("a ** b :", a ** b);                            //output: 10000


// Unary Operator: increment(++), decrement(--)
a++;                    // a = a + 1
console.log(a);                                             //output: 11
b--;                    // b = b - 1
console.log(b);                                             //output: 3

// pre increment/decrement: ++a, --b
// post increment/decrement: a++, b--